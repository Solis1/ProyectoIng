
package clases;

public class Doctores {
    
    private int iddoctores;
    private String nombre, apeidoPaterno, apeidoMaterno;
    private String telefono, direccion;

    public int getIddoctores() {
        return iddoctores;
    }

    public void setIddoctores(int iddoctores) {
        this.iddoctores = iddoctores;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getApeidoPaterno() {
        return apeidoPaterno;
    }

    public void setApeidoPaterno(String apeidoPaterno) {
        this.apeidoPaterno = apeidoPaterno;
    }

    public String getApeidoMaterno() {
        return apeidoMaterno;
    }

    public void setApeidoMaterno(String apeidoMaterno) {
        this.apeidoMaterno = apeidoMaterno;
    }

    public String getTelefono() {
        return telefono;
    }

    public void setTelefono(String telefono) {
        this.telefono = telefono;
    }

    public String getDireccion() {
        return direccion;
    }

    public void setDireccion(String direccion) {
        this.direccion = direccion;
    }
    
    
    
}
